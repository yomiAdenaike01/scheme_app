<template>
  <div v-if="team.length > 0" class="comms_container">
    <Chats />
    <ActiveChat
      v-if="Object.values(activeChat).length > 0"
      :user-to-message="userToMessage"
    />
    <div v-else class="no_active_chat">
      <h1>No chats selected</h1>
      <i class="bx bxl-discourse"></i>
    </div>
  </div>
  <div v-else class="no_content center">
    <TextDisplay
      :display-text="{
        heading: 'No team members',
        hasIcon: true,
        content: 'To create a team member please go to user management'
      }"
    >
      <i slot="header" class="bx bx-user"></i>
      <el-button slot="body" type="text" @click="$router.push({ name: 'team' })"
        >Go to user management</el-button
      >
    </TextDisplay>
  </div>
</template>

<script>
import { mapActions, mapMutations, mapState } from "vuex";
import Chats from "./components/Chats";
import ActiveChat from "./components/ActiveChat";
import TextDisplay from "@/components/TextDisplay";
export default {
  name: "CommsModule",
  components: {
    Chats,
    ActiveChat,
    TextDisplay
  },
  computed: {
    ...mapState(["globalIntervals"]),
    ...mapState("Team", ["team"]),
    ...mapState("Comms", ["activeChat"]),
    userToMessage() {
      return this.$route.params?.userToMessage;
    }
  },
  created() {
    this.CREATE_GLOBAL_INTERVAL({
      id: "commsCalls",
      immediate: true,
      method: () => {
        return new Promise((resolve, reject) => {
          this.getChats()
            .then(() => {
              resolve();
            })
            .catch(err => {
              reject(err);
            });
        });
      },
      duration: this.globalIntervals.chat
    });
  },
  activated() {
    let chatID = this.$route.params?.chatID;
    if (chatID) {
      let chatIndex = this.chats.findIndex(chat => {
        return chat._id == chatID;
      });
      this.UPDATE_ACTIVE_CHAT(this.chats[chatIndex]);
    }
  },
  methods: {
    ...mapActions("Comms", ["getChats"]),
    ...mapMutations(["CREATE_GLOBAL_INTERVAL"]),
    ...mapMutations("Comms", ["UPDATE_ACTIVE_CHAT"])
  }
};
</script>


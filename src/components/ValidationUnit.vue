<template>
  <div class="validation_wrapper shadow">
    <el-tag
      class="validation_unit txt_center"
      :type="booleanController"
      effect="plain"
      >{{ this[booleanController]["text"] }}</el-tag
    >
  </div>
</template>

<script>
import { mapGetters } from "vuex";
export default {
  name: "ValidationUnit",

  props: {
    text: {
      type: String
    },

    success: {
      type: Object
    },
    danger: {
      type: Object
    },
    info: {
      type: Object
    }
  },
  computed: {
    booleanController() {
      return this.success.condition
        ? "success"
        : this.danger.condition
        ? "danger"
        : this.info.condition
        ? "info"
        : "info";
    }
  }
};
</script>
<style lang="scss" scoped>
.validation_unit {
  &/deep/ {
    .el-tag {
      width: 100px;
    }
  }
}
</style>

<template>
  <div class="comments_container">
    <div v-if="mode == 'overview'" class="overview_wrapper">
      <i class="bx bx-message-rounded grey"></i>
      <small class="grey">{{ commentCount }}</small>
    </div>
    <div v-if="mode != 'overview'" class="full_mode_container">
      <div
        v-if="comments.length > 0"
        ref="comments_wrapper"
        class="comments_wrapper"
      >
        <div
          v-for="(comment, index) in comments"
          :key="comment._id"
          class="comment"
        >
          <div class="comment_header">
            <Avatar :name="comment.assignedTo.name" />
            <small class="username">{{ comment.assignedTo.name }}</small>
            <small class="timestamp">{{
              initMoment(comment.dateCreated).calendar()
            }}</small>
          </div>

          <div class="comment_message" @click="editMessage = true">
            <p>{{ comment.message }}</p>
          </div>
          <div v-show="canInteract" class="comment_footer">
            <el-button
              class="delete_comment"
              icon="el-icon-delete"
              circle
              @click="
                $emit('deleteComment', {
                  commentIndex: index,
                  _id: comment._id
                })
              "
            ></el-button>
          </div>
        </div>
      </div>

      <div v-if="canInteract" class="create_comment_wrapper">
        <Avatar class="comment_avatar" :name="userInformation.name" />
        <el-input
          v-model="newMessage"
          class="input_comment"
          placeholder="Write a comment..."
          @keyup.enter.native="createComment"
        ></el-input>
        <el-button
          type="text"
          :disabled="newMessage.length == 0"
          @click="createComment"
          >Post</el-button
        >
      </div>
    </div>
  </div>
</template>

<script>
import { mapGetters, mapState } from "vuex";
export default {
  name: "Comments",
  components: {
    ActionIcon: () => import("@/components/ActionIcon"),
    Avatar: () => import("@/components/Avatar")
  },
  props: {
    mode: {
      type: String,
      default: "overview"
    },
    comments: {
      type: Array,
      default: () => []
    },
    commentCount: {
      type: Number,
      default: 0
    },
    canInteract: {
      type: Boolean,
      default: false
    }
  },
  data() {
    return {
      editMessage: false,
      commentMessage: "",
      newMessage: "",
      displayActions: false
    };
  },
  computed: {
    ...mapGetters(["getIsAdmin"]),
    ...mapState(["userInformation"])
  },

  methods: {
    createComment() {
      this.$emit("createComment", {
        ref: this.$refs.comments_wrapper,
        message: this.newMessage
      }),
        this.reset();
    },
    reset() {
      this.newMessage = "";
      this.commentMessage = "";
      this.editMessage = false;
    },
    handleDisplayActions(userID) {
      if (this.getIsAdmin || userID == this.userInformation._id) {
        this.displayActions = true;
      }
    },

    updateMessage(message, _id) {
      if (
        message.toLowerCase().trim() != this.commentMessage.toLowerCase().trim()
      ) {
        this.$emit("updateComment", { comment: this.commentMessage, _id });
        this.editMessage = false;
      } else {
        this.editMessage = false;
      }
    }
  }
};
</script>

